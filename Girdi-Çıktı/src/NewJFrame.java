    private void ComboUrunCesidi2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ComboUrunCesidi2ActionPerformed
        // TODO add your handling code here:
        
        
        DefaultTableModel model = (DefaultTableModel) tabloModel.getModel();
        String secilenDeger = (String) ComboUrunCesidi2.getSelectedItem(); 
        
        TableRowSorter<DefaultTableModel> sorter = new TableRowSorter<>(model);
        tabloModel.setRowSorter(sorter);
        
        if (secilenDeger.equals("Tümü")) {
            sorter.setRowFilter(null); 
        } else {
            sorter.setRowFilter(RowFilter.regexFilter(secilenDeger)); 
        }
    }//GEN-LAST:event_ComboUrunCesidi2ActionPerformed

