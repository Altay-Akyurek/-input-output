    private void b1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b1ActionPerformed
        // TODO add your handling code here:
        String urunTipi= (String)ComboUrunCesidi.getSelectedItem();
        String urunAdi=txtUrunAdi.getText().trim();
        String fiyatStr=txtBirimFiyat.getText().trim();
        
        if(!cbSatis.isSelected()&& !cbAlis.isSelected())
        {
            JOptionPane.showMessageDialog(this, "lütfen tüm alanları dolduruurnuz");
            return;
        }
        try
        {
            int miktar =(int)spinnerMiktar.getValue();
            double birimFiyati=Double.parseDouble(fiyatStr);
            double tutar=miktar*birimFiyati;
            
            l1.setText("Tutar:"+tutar+"TL");

            String islemTipi="";
            if(cbSatis.isSelected()&&!cbAlis.isSelected())
            {
                islemTipi="SATIŞ";
                
            }
            else if(cbAlis.isSelected()&&!cbSatis.isSelected())
            {
                islemTipi="ALİŞ";
                
            }
            else
            {
                JOptionPane.showMessageDialog(this, "yanlızca bir işlem tipi giriniz:");
                
            }
            
        }
        catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Lütfen geçerli bir sayı girin.");
}
        String ÜrünAdi=txtUrunAdi.getText();
        ComboUrunCesidi2.addItem(ÜrünAdi);
        
        //tabloya ekleme
        int BirimFiyat = Integer.parseInt(txtBirimFiyat.getText());
        int Miktar = (int)spinnerMiktar.getValue();
        int tutar = Miktar*BirimFiyat;
        DefaultTableModel model = (DefaultTableModel) tabloModel.getModel();
        
        
        if (cbSatis.isSelected()) {
           Object[] bilgiler = {txtUrunAdi.getText(),spinnerMiktar.getValue(),tutar,"satış"};
            model.addRow(bilgiler); 
        }else if(cbAlis.isSelected()) {
            Object[] bilgiler = {txtUrunAdi.getText(), spinnerMiktar.getValue(), tutar, "alış"};
            model.addRow(bilgiler);
}
        
    }//GEN-LAST:event_b1ActionPerformed

